import math

sqrt_ours = math.sqrt(21)
print(sqrt_ours)
cube_root = math.cbrt(21)
print(cube_root)

fourth_root = 21 ** (1/4)
print(fourth_root)

fifth_root = 21 ** (1/5)
print(fifth_root)

sixth_root = 21 ** (1/6)
print(sixth_root)
array_of_sequential_roots = [sqrt_ours, cube_root, fourth_root, fifth_root, sixth_root]
print(array_of_sequential_roots)